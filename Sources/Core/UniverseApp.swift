import SwiftUI

/// Одно приложение «Вселенной» (Блокнот, Композитор, …).
///
/// Каждое приложение — самостоятельный модуль с одним корневым экраном. Оболочка
/// `UniverseShellView` показывает их вкладками, а отдельные обёртки (позже) запускают
/// тот же модуль как standalone-приложение. Общий фундамент (хранилище, граф, тема)
/// инжектится оболочкой через environment — модулю его передавать не нужно.
protocol UniverseApp: Identifiable {
    /// Стабильный идентификатор приложения (для выбора активной вкладки).
    var id: String { get }
    /// Отображаемое имя на вкладке.
    var title: String { get }
    /// SF Symbol иконки вкладки.
    var icon: String { get }
    /// Корневой экран модуля. AnyView — чтобы держать разнородные приложения в одном
    /// списке (existential), пока без отдельных скомпилированных библиотек.
    func makeRootView() -> AnyView
}
